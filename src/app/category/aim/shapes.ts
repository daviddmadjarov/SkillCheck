let colorIdx = 0;
function nextColor() {
  const c = C[colorIdx % C.length];
  colorIdx = (colorIdx + 1) % C.length;
  return c;
}

export type SplitShapeDef = {
  pts: Point[];
  label: string;
  fill: string;
  stroke: string;
  path: string;
};

function pat(label: string, ...list: number[]): SplitShapeDef {
  const pts: Point[] = [];
  for (let i = 0; i < list.length; i += 2) pts.push({ x: list[i], y: list[i + 1] });
  const col = nextColor();
  return { pts, label, path: vertPath(pts), ...col };
}

